void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim)
{
    if (htim->Instance == TIM4)
    {
        button_ticks();
        if (MPU_READ)
        {
            MPU6050_Read_All(&mpu6050Data);
        }
    }
    if (htim == &htim2)
    {
        if (READ_SPEED == 1)
        {
            Motor_Speed();
        }
        else
        {
            Update_Motor_Speed(target_speed);
        }
    }
}

